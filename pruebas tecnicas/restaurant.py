estrellita = int(input("¿como calificarias el restaruante del 1-5 ⭐:"))

if estrellita >= 4.5:
    print("Extraordinario")

elif estrellita >= 4:
    print("Excelente")

elif estrellita >= 3:
    print("Bueno")

elif estrellita >= 2:
    print("Regular")

else:
    print("Pesimo")